    private void RegBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegBtnActionPerformed
        String mobile = mobField.getText();
        String fName = fnameField.getText();
        String lName = lnameField.getText();
        String email = emailField.getText();
        String company = String.valueOf(companyCombo.getSelectedItem());
        
        if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Mobile Number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(this, "Invalid Mobile Number!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (fName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your First Name!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your Last Name!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your Email Address!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9\\+_-]+(\\.[A-Za-z0-9\\+_-]+)*@[^-][A-Za-z0-9\\+-]+(\\.[A-Za-z0-9\\+-]+)*(\\.[A-Za-z]{2,})$")) {
            JOptionPane.showMessageDialog(this, "Please Enter Valid Email Address!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (company.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select a Company!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            
            try {
                
                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `dealer` WHERE `mobile`='" + mobile + "'");
                
                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "This Dealer Already Registered!", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.executeIUD("INSERT INTO `dealer` (`mobile`,`first_name`,`last_name`,`email`,`company_id`) "
                            + "VALUES ('" + mobile + "','" + fName + "','" + lName + "','" + email + "','" + companyMap.get(company) + "')");
                    JOptionPane.showMessageDialog(this, "Dealer Registered!", "Success", JOptionPane.WARNING_MESSAGE);
                    
                    loadDealers();
                    reset();
                }
                
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }

    }//GEN-LAST:event_RegBtnActionPerformed

