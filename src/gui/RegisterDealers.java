    public void loadCompanies() {
        try {
            
            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `company`");
            
            Vector<String> vector = new Vector<>();
            vector.add("Select");
            
            while (resultSet.next()) {
                vector.add(resultSet.getString("name"));
                companyMap.put(resultSet.getString("name"), resultSet.getString("id"));
            }
            
            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            companyCombo.setModel(model);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    
    private void loadDealers() {
        try {
            
            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `dealer` "
                    + "INNER JOIN `company` ON `company`.`id`=`dealer`.`company_id`");
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            
            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("first_name"));
                vector.add(resultSet.getString("last_name"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("company.name"));
                
                model.addRow(vector);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

