    private void AddresssUpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddresssUpBtnActionPerformed
        String line1 = aLine1.getText();
        String line2 = aLine2.getText();
        String city = String.valueOf(cityCombo.getSelectedItem());

        if (line1.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Address line1", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (line2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Address line2", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (city.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a city", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            for (int i = 0; i < addressTable.getRowCount(); i++) {
                String getLine1 = String.valueOf(addressTable.getValueAt(i, 1));
                String getLine2 = String.valueOf(addressTable.getValueAt(i, 2));
                String getCity = String.valueOf(addressTable.getValueAt(i, 3));

                if (getLine1.equals(line1) && getLine2.equals(line2) && getCity.equals(city)) {

                    JOptionPane.showMessageDialog(this, "Updated Succesfully!", "Warning", JOptionPane.WARNING_MESSAGE);

                    loadAddress();
                    reset();
                } else {

                    try {

                        MySQL.executeIUD("UPDATE `employee_address` SET "
                                + "`line1`='" + line1 + "',`line2`='" + line2 + "',`city_id`='" + cityMap.get(city) + "' "
                                + "WHERE `employee_email`='" + this.email + "'");

                    } catch (Exception e) {
                        e.printStackTrace();
                    }

                    loadAddress();
                    reset();
                }
            }

        }
    }//GEN-LAST:event_AddresssUpBtnActionPerformed

    private void addressAddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addressAddBtnActionPerformed
        String line1 = aLine1.getText();
        String line2 = aLine2.getText();
        String city = String.valueOf(cityCombo.getSelectedItem());
        long id = System.currentTimeMillis();
        String cm = "CM_EA_";

        if (line1.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Address line1", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (line2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Address line2", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (city.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a city", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {

//                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `employee` WHERE `email`='" + this.email + "'");
                boolean isFound = false;

                for (int i = 0; i < addressTable.getRowCount(); i++) {

                    String getLine1 = String.valueOf(addressTable.getValueAt(i, 1));
                    String getLine2 = String.valueOf(addressTable.getValueAt(i, 2));
                    String getCity = String.valueOf(addressTable.getValueAt(i, 3));

                    if (getLine1.equals(line1) && getLine2.equals(line2) && getCity.equals(city)) {
                        JOptionPane.showMessageDialog(this, "Address already added", "Warning", JOptionPane.WARNING_MESSAGE);
                        isFound = true;
                        break;
                    }
                }

                if (!isFound) {

                    MySQL.executeIUD("INSERT INTO `employee_address` (`id`,`line1`,`line2`,`city_id`,`employee_email`) "
                            + "VALUE ('" + cm + "" + id + "','" + line1 + "','" + line2 + "','" + cityMap.get(city) + "','" + this.email + "')");

                    loadAddress();
                    reset();

                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_addressAddBtnActionPerformed

