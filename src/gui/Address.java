    public void loadAddress() {
        try {

            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `employee_address` "
                    + "INNER JOIN `city` ON `employee_address`.`city_id` = `city`.`id` "
                    + "INNER JOIN `employee` ON `employee_address`.`employee_email` = `employee`.`email` "
                    + "WHERE `email`='" + this.email + "'");

            DefaultTableModel model = (DefaultTableModel) addressTable.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("id"));
                vector.add(resultSet.getString("line1"));
                vector.add(resultSet.getString("line2"));
                vector.add(resultSet.getString("city.name"));
                vector.add(resultSet.getString("employee.first_name"));

                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadCities() {
        try {

            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `city`");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultSet.next()) {
                vector.add(resultSet.getString("name"));
                cityMap.put(resultSet.getString("name"), resultSet.getString("id"));
            }
            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            cityCombo.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

