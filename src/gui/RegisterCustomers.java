    public void loadJobRole() {
        try {
            
            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `employee` "
                    + "INNER JOIN `employee_type` ON `employee_type`.`id`=`employee`.`employee_type_id` WHERE `email`='" + empEmail.getText() + "'");
            
            if (resultSet.next()) {
                String jobRole = resultSet.getString("name");
                empJob.setText(jobRole);
                
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void loadGender() {
        
        try {
            
            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `gender`");
            
            Vector<String> vector = new Vector<>();
            vector.add("Select");
            
            while (resultSet.next()) {
                vector.add(resultSet.getString("name"));
                genderMap.put(resultSet.getString("name"), resultSet.getString("id"));
            }
            
            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            genderCombo.setModel(model);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }
    
    private void loadCities() {
        try {
            
            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `city`");
            
            Vector<String> vector = new Vector<>();
            vector.add("Select");
            
            while (resultSet.next()) {
                vector.add(resultSet.getString("name"));
                cityMap.put(resultSet.getString("name"), resultSet.getString("id"));
            }
            
            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            cityCombo.setModel(model);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void loadCustomers() {
        try {
            
            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `customer` INNER JOIN "
                    + "`gender` ON `gender`.`id`=`customer`.`gender_id` "
                    + "INNER JOIN `city` ON `city`.`id`=`customer`.`city_id`");
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            
            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("first_name"));
                vector.add(resultSet.getString("last_name"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("gender.name"));
                vector.add(resultSet.getString("city.name"));
                
                model.addRow(vector);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

