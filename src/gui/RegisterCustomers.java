    private void regCustBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regCustBtnActionPerformed
        String mobile = mobField.getText();
        String fName = fNField.getText();
        String lName = lNField.getText();
        String email = emailField.getText();
        String gender = String.valueOf(genderCombo.getSelectedItem());
        String city = String.valueOf(cityCombo.getSelectedItem());
        
        if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!mobile.matches("^(?:0|94|\\+94|0094)?(?:(11|21|23|24|25|26|27|31|32|33|34|35|36|37|38|41|45|47|51|52|54|55|57|63|65|66|67|81|91)(0|2|3|4|5|7|9)|7(0|1|2|4|5|6|7|8)\\d)\\d{6}$")) {
            JOptionPane.showMessageDialog(this, "Please enter valid mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (fName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter first name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter last name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9\\+_-]+(\\.[A-Za-z0-9\\+_-]+)*@[^-][A-Za-z0-9\\+-]+"
                + "(\\.[A-Za-z0-9\\+-]+)*(\\.[A-Za-z]{2,})$")) {
            JOptionPane.showMessageDialog(this, "Please enter valid email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (gender.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select a Gender", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (city.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select a City", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            
            try {
                
                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `customer` WHERE `mobile`='" + mobile + "' OR `email`='" + email + "'");
                
                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Customer Already Registered!", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    
                    MySQL.executeIUD("INSERT INTO `customer` (`mobile`,`first_name`,`last_name`,`email`,`gender_id`,`city_id`) "
                            + "VALUES ('" + mobile + "','" + fName + "','" + lName + "','" + email + "',"
                            + "'" + genderMap.get(gender) + "','" + cityMap.get(city) + "')");
                    
                    JOptionPane.showMessageDialog(this, "Customer Registered!", "Warning", JOptionPane.WARNING_MESSAGE);
                    
                    loadCustomers();
                    reset();
                    
                }
                
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }
        

    }//GEN-LAST:event_regCustBtnActionPerformed

