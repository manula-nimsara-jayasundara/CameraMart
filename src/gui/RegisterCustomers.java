    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        String mobile = mobField.getText();
        String fName = fNField.getText();
        String lName = lNField.getText();
        String email = emailField.getText();
        String gender = String.valueOf(genderCombo.getSelectedItem());
        String city = String.valueOf(cityCombo.getSelectedItem());
        
        if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your mobile", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(this, "Please enter valid mobile", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (fName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your first name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your last name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (gender.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a Gender", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (city.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a City", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            
            
            try {
                
                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `customer` WHERE `mobile` = '" + mobile + "' OR `email`='" + email + "'");
                
                boolean canUpdate = false;
                
                if (resultSet.next()) {
                    
                    if (!resultSet.getString("email").equals(email)) {
                        
                        JOptionPane.showMessageDialog(this, "This Email or Mobile Number already used", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {
                        canUpdate = true;
                    }
                    
                } else {
                    canUpdate = true;
                }
                
                if (canUpdate) {
                    
                    MySQL.executeIUD("UPDATE `customer` SET `mobile`='" + mobile + "',"
                            + "`first_name`='" + fName + "',`last_name`='" + lName + "',"
                            + "`email`='" + email + "',`gender_id`='" + genderMap.get(gender) + "',"
                            + "`city_id`='" + cityMap.get(city) + "' WHERE `email`='" + email + "'");
                    
                    JOptionPane.showMessageDialog(this, "Updated Successfuly!", "Success", JOptionPane.WARNING_MESSAGE);
                    
                    loadCustomers();
                    reset();
//                    jPanel4.setVisible(true);
                }
                
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_updateBtnActionPerformed

